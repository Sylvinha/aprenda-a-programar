let numero = 17
// operador resto: %
// saber se é primo ou não
//faz o resto por 1. se o resto for 0 , significa que ele é divisível. 

if (numero % 1 == 0){
    console.log(`Sim, o número ${numero} é primo!`)
} else{
    console.log(`Não, o número ${numero} não é primo`)
}

